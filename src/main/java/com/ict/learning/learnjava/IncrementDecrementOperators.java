package com.ict.learning.learnjava;

public class IncrementDecrementOperators {

	public static void main(String[] args) {
		
   int a=10;
   int b=5;
  // Increment
   System.out.println("value of a is " +a);
   System.out.println("value of a++ is "+a++);
   System.out.println("value of a is " +a);
   System.out.println();
   
   
   System.out.println("value of a is " +a);
   System.out.println("value of ++a is " + ++a);
   System.out.println("value of a is "+a);
   System.out.println();
   
   
	// Decrement
   System.out.println("value of b is " +b);
   System.out.println("value of b-- is " +b--);
   System.out.println("value of b is "+b);
   System.out.println();
   
   System.out.println("value of b is " +b);
   System.out.println("value of --b is " +--b);
   System.out.println("value of b is "+b);	
		
		
		
	}

}
